void Delay500us()		//@12.000MHz
{
	unsigned char i;

	_nop_();
	i = 247;
	while (--i);
}


unsigned char inverted (unsigned char oldVal){
    unsigned int i;
    unsigned int newVal=0x00;
    unsigned int x=0x01;
    for(i=0;i<8;i++){
        newVal += !!(oldVal & x);
		if(i<7) newVal=newVal<<1;
        x=x<<1;
    }
	return newVal;
}

